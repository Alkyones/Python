#Basic Operators and Control Structures

#Arithmetic Operators
a = int(input('Enter a number: '))
b = int(input( 'Enter a number: '))
print(type(a))
print(type(b))
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)

#Comparison Operators
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

#Logical Operators
print(a > 0 and b > 0)
print(a > 0 or b > 0)
